from employee import Employee

# ---------------------------------------------------------------------------------------------------------------------
# Class(s)
# ---------------------------------------------------------------------------------------------------------------------
class SalaryEmployee(Employee):
  def __init__(self, first_name, last_name, salary):
    super().__init__(first_name, last_name)
    self.salary = salary

  def calculate_weekly_paycheck(self):
    return self.salary / 52
   
