# ---------------------------------------------------------------------------------------------------------------------
# Function(s)
def addition(a, b):
  return a + b

# ---------------------------------------------------------
def main():
  a = float(input('Enter your 1st number: '))
  b = float(input('Enter your 2nd number: '))

  sum = addition(a, b)

  print(f"{a} + {b} = {sum}")

# ---------------------------------------------------------------------------------------------------------------------
main()

